"""Aggregate benchmark checkpoints into CSV/JSON + a LaTeX table + summary.

Usage: python aggregate.py
Outputs land in AgentBench\results\.
"""
import csv
import json
import statistics
from pathlib import Path

import config

MODEL_LABEL = {"claude-opus-4-8": "Opus 4.8",
               "claude-sonnet-4-6": "Sonnet 4.6",
               "claude-haiku-4-5": "Haiku 4.5"}


def _checkpoints():
    rows = []
    for model_id in config.MODELS.values():
        mdir = config.RUNS_DIR / model_id
        if not mdir.exists():
            continue
        for run_dir in sorted(mdir.glob("run[0-9]*")):
            if run_dir.name.count("_attempt"):
                continue
            cp = run_dir / "checkpoint.json"
            if cp.exists():
                rows.append(json.loads(cp.read_text(encoding="utf-8")))
    return rows


def _fmt_gains(g):
    if not g:
        return "-"
    return ("(" + ",".join(f"{g[k]:g}" for k in config.GAIN_NAMES) + ")")


def write_csv(rows, path):
    cols = ["model", "run_id", "status", "overall_pass", "first_pass_iteration",
            "iterations_used", "final_n_pass", "Kqp", "Kqi", "Kvp", "Kvi",
            "input_tokens", "output_tokens", "est_cost_usd", "elapsed_s"]
    with open(path, "w", newline="", encoding="utf-8") as f:
        w = csv.writer(f)
        w.writerow(cols)
        for r in rows:
            g = r.get("final_gains") or {}
            u = r.get("usage") or {}
            w.writerow([
                r.get("model"), r.get("run_id"), r.get("status"),
                r.get("overall_pass"), r.get("first_pass_iteration"),
                r.get("iterations_used"), r.get("final_n_pass"),
                g.get("Kqp"), g.get("Kqi"), g.get("Kvp"), g.get("Kvi"),
                u.get("input_tokens"), u.get("output_tokens"),
                u.get("est_cost_usd"), r.get("elapsed_s"),
            ])


def per_model(rows):
    out = {}
    for model_id in config.MODELS.values():
        mr = [r for r in rows if r.get("model") == model_id]
        if not mr:
            continue
        passes = [r for r in mr if r.get("overall_pass")]
        ftp = [r["first_pass_iteration"] for r in passes
               if r.get("first_pass_iteration") is not None]
        costs = [r["usage"]["est_cost_usd"] for r in mr
                 if r.get("usage") and r["usage"].get("est_cost_usd") is not None]
        npass = [r.get("final_n_pass") or 0 for r in mr]

        def med_gain(k):
            vals = [r["final_gains"][k] for r in passes
                    if r.get("final_gains") and k in r["final_gains"]]
            return round(statistics.median(vals), 3) if vals else None

        out[model_id] = {
            "n_runs": len(mr),
            "pass_at_n": len(passes),
            "pass_rate": round(len(passes) / len(mr), 3) if mr else 0,
            "median_iters_to_pass": (statistics.median(ftp) if ftp else None),
            "median_final_n_pass": statistics.median(npass) if npass else 0,
            "median_gains_passing": {k: med_gain(k) for k in config.GAIN_NAMES}
                                    if passes else None,
            "mean_cost_usd": round(statistics.mean(costs), 4) if costs else None,
            "total_cost_usd": round(sum(costs), 4) if costs else None,
            "errors": sum(1 for r in mr if r.get("status") == "failed"),
        }
    return out


def write_latex(stats, rows, path):
    """LaTeX table matching dynamic.tex tab:dmq_case_results (Agent/Outcome/
    Final gains/Main observation), reporting median-of-N outcomes."""
    lines = [
        r"% Generated by AgentBench/harness/aggregate.py - median over N runs",
        r"\begin{table}[t]",
        r"\caption{DMQ case-study results on the modified WECC solar model "
        r"(median over %d runs per model).}" % config.RUNS_PER_MODEL,
        r"\label{tab:dmq_case_results}",
        r"\centering",
        r"\scriptsize",
        r"\setlength{\tabcolsep}{2pt}",
        r"\begin{tabular}{@{}p{0.12\columnwidth}p{0.20\columnwidth}"
        r"p{0.22\columnwidth}p{0.34\columnwidth}@{}}",
        r"\toprule",
        r"Agent & Outcome & Median final gains & Main observation \\",
        r"\midrule",
    ]
    for model_id in config.MODELS.values():
        if model_id not in stats:
            continue
        s = stats[model_id]
        label = MODEL_LABEL.get(model_id, model_id)
        passed = s["pass_at_n"]
        n = s["n_runs"]
        if passed:
            mi = s["median_iters_to_pass"]
            outcome = f"Pass {passed}/{n} (median iter.\\ {mi:g})"
            mg = s["median_gains_passing"]
            gains = "$(%s)$" % ",".join(f"{mg[k]:g}" for k in config.GAIN_NAMES)
            obs = "Reducing the reactive/voltage gains removes the oscillations; all 9 tests pass."
        else:
            outcome = f"Fail {n - passed}/{n}"
            gains = "No passing set"
            obs = "Oscillations / LVRT momentary cessation persist; tests remain failed."
        lines.append(f"{label} & {outcome} & {gains} & {obs} \\\\")
    lines += [r"\bottomrule", r"\end{tabular}", r"\end{table}"]
    Path(path).write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_summary(stats, rows, path):
    lines = ["# DMQ Benchmark Results\n",
             f"Runs per model: {config.RUNS_PER_MODEL}; "
             f"iteration budget: {config.ITERATION_BUDGET}.\n"]
    for model_id, s in stats.items():
        lines.append(f"## {MODEL_LABEL.get(model_id, model_id)} (`{model_id}`)")
        lines.append(f"- pass@{s['n_runs']}: **{s['pass_at_n']}/{s['n_runs']}** "
                     f"(rate {s['pass_rate']})")
        lines.append(f"- median iterations to pass: {s['median_iters_to_pass']}")
        lines.append(f"- median final tests passed: {s['median_final_n_pass']}/8")
        lines.append(f"- median passing gains: {s['median_gains_passing']}")
        lines.append(f"- mean cost/run: ${s['mean_cost_usd']}; "
                     f"total: ${s['total_cost_usd']}; errors: {s['errors']}\n")
    lines.append("## Per-run gain trajectories\n")
    for r in rows:
        traj = " -> ".join(_fmt_gains(h["gains"]) + f"[{h['n_pass']}/8]"
                           for h in (r.get("history") or []))
        lines.append(f"- {MODEL_LABEL.get(r['model'], r['model'])} "
                     f"run{r['run_id']:02d}: pass={r.get('overall_pass')} | {traj}")
    Path(path).write_text("\n".join(lines) + "\n", encoding="utf-8")


def main():
    config.RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    rows = _checkpoints()
    rows.sort(key=lambda r: (r.get("model", ""), r.get("run_id", 0)))
    stats = per_model(rows)

    write_csv(rows, config.RESULTS_DIR / "aggregate.csv")
    (config.RESULTS_DIR / "aggregate.json").write_text(
        json.dumps({"per_model": stats, "runs": rows}, indent=2, default=str),
        encoding="utf-8")
    write_latex(stats, rows, config.RESULTS_DIR / "dmq_table.tex")
    write_summary(stats, rows, config.RESULTS_DIR / "summary.md")

    print(json.dumps(stats, indent=2, default=str))
    print(f"\n{len(rows)} runs aggregated -> {config.RESULTS_DIR}")


if __name__ == "__main__":
    main()
