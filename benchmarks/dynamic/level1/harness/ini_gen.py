"""Generate the benchmark DMView project INI at the DMView root.

DMView resolves `<ProjectName>.ini` against its own root directory, so the
generated file must live there. `input_path` is a path relative to the DMView
root pointing at the active run's working folder (which holds the case copies
Solar.sav / Solar.dyr).
"""
from pathlib import Path

import config

_TEMPLATE = """/PowerAgentBench-Dyn DMQ benchmark project (generated by AgentBench harness - do not edit)

[Build FS]
Build_FS_flag = 1

[Input files]
input_path = {input_path}
unconv_casefile = Solar.sav
model_file_lst = ['Solar.dyr']

[Tests]
{tests}

[Settings]
Plot_Flag = 0
Out2CSV_Flag = 1
Plot_SCR_scale = [1, 6]
Fault_Duration = 0.066667 /for SCR2 test
PSSE_VER = 36 /this machine has PSS/e 36 only
"""


def write_benchmark_ini(workdir: Path) -> Path:
    """Point the benchmark INI at `workdir` (absolute or DMView-root-relative)."""
    workdir = Path(workdir)
    if workdir.is_absolute():
        rel = workdir.relative_to(config.DMVIEW_ROOT)
    else:
        rel = workdir
    tests = "\n".join(f"{name} = {spec}" for name, spec in config.TESTS)
    config.INI_PATH.write_text(
        _TEMPLATE.format(input_path=str(rel), tests=tests), encoding="utf-8"
    )
    return config.INI_PATH
